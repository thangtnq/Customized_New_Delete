#ifndef TYPES_H
#define TYPES_H

class Type
{
public:
	typedef	bool			Bool;	// Wrapper for boolean type
	typedef	float			Float;	// Wrapper for single precision float type
	typedef signed char		S8;		// Wrapper for signed char type
	typedef unsigned char	U8;		// Wrapper for unsigned char type
	typedef	char			Char;	// Wrapper for char type
	typedef signed short	S16;	// Wrapper for signed short type
	typedef unsigned short  U16;	// Wrapper for unsigned short type
	typedef	unsigned int	U32;	// Wrapper for unsigned int type
	typedef	signed int		S32;	// Wrapper for signed int type
	typedef	signed int		Int;	// Wrapper for signed int type
	typedef	unsigned int	UInt;	// Wrapper for unsigned int type
	typedef	void			Void;	// Wrapper for void type
};

#endif 

